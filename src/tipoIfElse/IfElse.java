package tipoIfElse;

public class IfElse {

	public static void main(String[] args) {
		
		boolean estaChovendo = false; // usamos boolean para definir se é "true ou false".
		
		if (estaChovendo) {
			System.out.println("Está chovendo");
		} else {
			System.out.println("Sem chuva hoje, não precisa de guarda-chuva");
		}
	}
	
}
