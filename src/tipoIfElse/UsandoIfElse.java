public class UsandoIfElse {

	public static void main(String[] args) {
		
		int clima = 2;
		
		if (clima == 1) {
		System.out.println("traga um guarda-chuva");
		} else if (clima == 2) {
		System.out.println("Use óculos de sol");
		} else {
			System.out.println("Apenas saia normalmente");
		}
	}
}